# Variables and basic data types

# Integers
x = 10

# Floats
pi = 3.14159

# Strings
name = "Alice"

# Boolean
is_active = True

print("Values:", x, pi, name, is_active)
print("Types:", type(x), type(pi), type(name), type(is_active))

# Multiple assignments
a, b, c = 1, 2.5, "hello"
print(a, b, c)
